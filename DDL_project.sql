DROP TABLE IF EXISTS COMPETITION;
DROP TABLE IF EXISTS VENUE;
DROP TABLE IF EXISTS TEAM;
DROP TABLE IF EXISTS LIFT;
DROP TABLE IF EXISTS LIFTER;

CREATE TABLE CATEGORY (
	CAT_ID INT PRIMARY KEY,
	CAT_NAME VARCHAR(100) NOT NULL,
	CAT_GENDER VARCHAR(1) NOT NULL,
	CAT_EXPERIENCE_MIN INT NOT NULL,
	CAT_EXPERIENCE_MAX INT NOT NULL
);

CREATE TABLE VENUE (
	VEN_ID INT PRIMARY KEY,
	VEN_NAME VARCHAR(100) NOT NULL,
	VEN_STREET_NUMBER INT NOT NULL,
	VEN_STREET_NAME VARCHAR(100) NOT NULL,
	VEN_CITY_NAME VARCHAR(100) NOT NULL,
	VEN_STATE_NAME VARCHAR(2) NOT NULL,
	VEN_ZIP_CODE INT NOT NULL
);

CREATE TABLE COMPETITION (
	COMPETITION_ID INT PRIMARY KEY,
	VEN_ID INT,
	COMPETITION_DATE DATE NOT NULL,
	COMPETITION_NAME VARCHAR(100) NOT NULL,
	COMPETITION_DRUG_STATUS ENUM('tested', 'untested') NOT NULL,
	FOREIGN KEY(VEN_ID) REFERENCES VENUE(VEN_ID)
);

CREATE TABLE TEAM (
	TEAM_ID INT PRIMARY KEY,
	TEAM_NAME VARCHAR(100) NOT NULL
);

CREATE TABLE LIFTER (
	LIFTER_ID INT PRIMARY KEY,
	TEAM_ID INT DEFAULT NULL,
	LIFTER_YOB INT NOT NULL,
	LIFTER_STATE VARCHAR(3) NOT NULL,
	LIFTER_GENDER VARCHAR(1) NOT NULL,
	LIFTER_FNAME VARCHAR(100) NOT NULL,
	LIFTER_LNAME VARCHAR(100) NOT NULL,
	LIFTER_DRUG_TEST ENUM('pass', 'fail') DEFAULT NULL,
	FOREIGN KEY(TEAM_ID) REFERENCES TEAM(TEAM_ID)
);

CREATE TABLE COMPETITION_LOG (
	LIFTER_ID INT,
	COMPETITION_ID INT,
	CAT_ID INT,
	COMP_LOG_LIFTER_BODYWEIGHT DECIMAL(5,2) NOT NULL,
	COMP_LOG_LIFTER_EXPERIENCE INT NOT NULL,
	PRIMARY KEY(LIFTER_ID, COMPETITION_ID), -- composite PK
	FOREIGN KEY(LIFTER_ID) REFERENCES LIFTER(LIFTER_ID),
	FOREIGN KEY(COMPETITION_ID) REFERENCES COMPETITION(COMPETITION_ID),
	FOREIGN KEY(CAT_ID) REFERENCES CATEGORY(CAT_ID)
);

CREATE TABLE LIFT (
	LIFT_ID VARCHAR(20) PRIMARY KEY,
	LIFT_NAME VARCHAR(13),
	LIFT_WEIGHT DECIMAL(4,1),
	LIFT_ATMPTNUM INT NOT NULL,
	LIFTER_ID INT,
	COMPETITION_ID INT,
	FOREIGN KEY(LIFTER_ID, COMPETITION_ID) REFERENCES COMPETITION_LOG(LIFTER_ID, COMPETITION_ID)
);

